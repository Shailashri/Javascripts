function mydesignation(x){
    if ((x >= 18) && (x <= 20)){
console.log("Junior Developer");
    } else if ((x >= 21) && (x <= 24)){
        console.log("Senior developer");
    } else if ((x >= 25) && (x <= 30)) {
        console.log("Assistant manager");
    } else if (x >= 31) {
        console.log("manager");
} else	{
    console.log("undefined");
}
}
mydesignation(25);